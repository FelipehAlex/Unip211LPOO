
public class DivisaoDouble {
	
	public static void main(String[] args) {
		
		int a = 5;
		int b = 2;
		
		int c = a/b;
		
		System.out.println("O resultado de " + a + " / " + b + " eh igual a " + c);
		
		double aReal = 5.0;
		double bReal = 3.0;
		
		double cReal = aReal/bReal;
		
		System.out.println();
		System.out.println("O resultado REAL de " + aReal + " / " + bReal + " eh igual a " + cReal);
		
	}
	

}
