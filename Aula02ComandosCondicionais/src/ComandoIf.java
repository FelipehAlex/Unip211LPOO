
public class ComandoIf {
	
	public static void main(String[] args) {
		
		int a = 5;
		int b = 3;
		
		if(a<b) {
			System.out.println("a eh menor que b");
		}
		
		if(b<a) {
			System.out.println("b eh menor que a");
		}
		
		int c = 5;
		
		if(a <= c) {
			System.out.println("a eh menor ou igual a c");
		}
		
		if(a >= c) {
			System.out.println("a eh maior ou igual a c");
		}
		
		
		
	}

}
